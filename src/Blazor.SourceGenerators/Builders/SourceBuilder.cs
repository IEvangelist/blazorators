namespace Blazor.SourceGenerators.Builders;

/// <summary>
/// Represents a builder for generating C# source code.
/// </summary>
[DebuggerDisplay("{ToSourceCodeString()}", Name = "{_options.TypeName}")]
internal sealed class SourceBuilder
{
    internal const char NewLine = '\n';
    private const string _twoNewLines = "\n\n";

    private readonly StringBuilder _builder = new();
    private readonly GeneratorOptions _options;
    private readonly bool _isService;

    private Indentation _indentation = new(0);

    /// <summary>
    /// Gets or sets the set of fields used by the source builder.
    /// </summary>
    internal ISet<string>? Fields { get; private set; }

    /// <summary>
    /// Gets or sets the set of methods used by the source builder.
    /// </summary>
    internal ISet<string>? Methods { get; private set; }

    /// <summary>
    /// Gets the current indentation level of the source builder.
    /// </summary>
    internal int IndentationLevel => _indentation.Level;

    /// <summary>
    /// Gets the implementation name, which is lazily initialized the first time it is accessed.
    /// </summary>
    internal string ImplementationName => field ??=
        _options.Implementation.ToImplementationName(_isService);

    /// <summary>
    /// Gets the interface name, which is lazily initialized the first time it is accessed.
    /// </summary>
    internal string InterfaceName => field ??=
        _options.Implementation.ToInterfaceName(_isService);

#pragma warning disable CS9264 // Non-nullable property must contain a non-null value when exiting constructor. Consider adding the 'required' modifier, or declaring the property as nullable, or adding '[field: MaybeNull, AllowNull]' attributes.
    internal SourceBuilder(GeneratorOptions options, bool isService = true)
#pragma warning restore CS9264 // Non-nullable property must contain a non-null value when exiting constructor. Consider adding the 'required' modifier, or declaring the property as nullable, or adding '[field: MaybeNull, AllowNull]' attributes.
    {
        _options = options;
        _isService = isService;
    }

    /// <summary>
    /// Appends the copy right header to the source builder.
    /// </summary>
    /// <returns>The updated source builder.</returns>
    internal SourceBuilder AppendCopyRightHeader()
    {
        _builder.Append("""
            // Copyright (c) David Pine. All rights reserved.
            // Licensed under the MIT License:
            // https://bit.ly/blazorators-license
            // Auto-generated by blazorators.


            """);

        return this;
    }

    internal SourceBuilder AppendUsingDeclarations(bool requiresValueTask = false)
    {
        // `using System;` is needed for `Action<>` / `Func<>` callback
        // parameters that flow into the emitted interface and
        // implementation. Most consumer projects ship with
        // `<ImplicitUsings>enable</ImplicitUsings>` and would otherwise
        // resolve them transparently, but generated code shouldn't
        // require that opt-in: projects that disable implicit usings
        // previously hit CS0246 on every callback-bearing interop
        // surface. Emit unconditionally so the generated files are
        // self-contained regardless of the host project's settings.
        _builder.Append(value: $"using System;{NewLine}");

        if (_options is { SupportsGenerics: true })
        {
            _builder.Append(value: $"using Blazor.Serialization.Extensions;{NewLine}");
            _builder.Append(value: $"using System.Text.Json;{NewLine}");
            _builder.Append(value: $"using System.Text.Json.Serialization.Metadata;{NewLine}");
        }

        // Server hosting always returns `ValueTask`-wrapped values, so
        // `System.Threading.Tasks` is required. WebAssembly hosting is
        // synchronous by default, but `Promise<T>` returns force the
        // async path even under WASM (see `MethodBuilderDetails.Create`),
        // so the namespace is required whenever any emitted method
        // returns a Promise. Without this, consumers compiling with
        // `<ImplicitUsings>disable</ImplicitUsings>` (or older SDKs that
        // don't auto-include `System.Threading.Tasks`) saw a CS0246
        // referencing the generated `ValueTask<T>` signature.
        if (!_options.IsWebAssembly || requiresValueTask)
        {
            _builder.Append($"using System.Threading.Tasks;{NewLine}");
        }

        _builder.Append(NewLine);

        return this;
    }

    internal SourceBuilder AppendNamespace(string namespaceString, bool isNullableContext = true)
    {
        if (isNullableContext)
        {
            _builder.Append($"#nullable enable{NewLine}");
        }

        _builder.Append($"namespace {namespaceString};{_twoNewLines}");

        return this;
    }

    internal SourceBuilder AppendPublicInterfaceDeclaration()
    {
        _builder.Append($"/// <summary>{NewLine}");
        _builder.Append($"/// Source generated interface definition of the <c>{_options.TypeName}</c> type.{NewLine}");
        _builder.Append($"/// </summary>{NewLine}");
        _builder.Append($"public partial interface {InterfaceName}{NewLine}");

        return this;
    }

    internal SourceBuilder AppendInternalImplementationDeclaration()
    {
        _builder.Append($"/// <inheritdoc />{NewLine}");
        _builder.Append($"internal sealed class {ImplementationName} : {InterfaceName}{NewLine}");

        return this;
    }

    internal SourceBuilder AppendImplementationCtor()
    {
        var javaScriptRuntime = _options.IsWebAssembly
            ? "IJSInProcessRuntime"
            : "IJSRuntime";

        _builder.Append($"{_indentation}internal readonly {javaScriptRuntime} _javaScript = null!;{_twoNewLines}");
        _builder.Append($"{_indentation}public {ImplementationName}({javaScriptRuntime} javaScript) =>{NewLine}");

        IncreaseIndentation();

        _builder.Append($"{_indentation}_javaScript = javaScript;{_twoNewLines}");

        DecreaseIndentation();

        return this;
    }

    internal SourceBuilder AppendOpeningCurlyBrace(bool increaseIndentation = false)
    {
        IncreaseIndentationImpl(increaseIndentation);

        _builder.Append($"{_indentation}{{{NewLine}");

        return this;
    }

    internal SourceBuilder AppendClosingCurlyBrace(bool decreaseIndentation = false)
    {
        DecreaseIndentationImpl(decreaseIndentation);

        _builder.Append($"{_indentation}}}{NewLine}");

        return this;
    }

    internal SourceBuilder AppendTripleSlashMethodComments(
        CSharpMethod method,
        bool extrapolateParameters = false,
        IndentationAdjustment adjustment = IndentationAdjustment.Noop)
    {
        AdjustIndentation(adjustment);
        var indent = _indentation.ToString();

        _builder.Append($"{indent}/// <summary>{NewLine}");

        var jsMethodName = method.RawName.LowerCaseFirstLetter();
        var func = $"{_options.Implementation}.{jsMethodName}";

        _builder.Append($"{indent}/// Source generated implementation of <c>{func}</c>.{NewLine}");
        var rootUrl = "https://developer.mozilla.org/docs/Web/API";
        var fullUrl = $"{rootUrl}/{_options.TypeName}/{jsMethodName}";
        _builder.Append($"{indent}/// <a href=\"{fullUrl}\"></a>{NewLine}");
        _builder.Append($"{indent}/// </summary>{NewLine}");

        if (extrapolateParameters)
        {
            foreach (var (index, param) in method.ParameterDefinitions.Select())
            {
                if (index.IsFirst)
                {
                    _builder.Append(
                        $"/// <param name=\"component\">The calling Razor (or Blazor) component.</param>{NewLine}");
                }

                if (param.ActionDeclaration is not null)
                {
                    var name = param.ToArgumentString();
                    // Use the canonical action type-argument list rather
                    // than `DependentTypes.Keys`. The former is the
                    // single source of truth for the action's generic
                    // arguments and is C#-mapped (TS `number` ->
                    // `double`, `boolean` -> `bool`, etc.) with arrays
                    // intact; the latter only carries *custom* types
                    // (primitives don't go through the declaration
                    // reader, and array element shapes are flattened
                    // to the element key), so the rendered doc string
                    // diverged from the actual delegate signature any
                    // time a callback had a primitive parameter (e.g.
                    // `FrameRequestCallback(time: number)` rendered
                    // `System.Action{}` instead of `System.Action{double}`).
                    var typeArgs = param.ActionDeclaration.MappedActionTypeArguments;
                    var actionRef = typeArgs.Count == 0
                        ? "System.Action"
                        : $"System.Action{{{string.Join(", ", typeArgs)}}}";
                    var action =
                        $"Expects the name of a <c>\"JSInvokableAttribute\"</c> C# method with the following " +
                        $"<c>{actionRef}</c>.";
                    _builder.Append(
                        $"/// <param name=\"{name}\">{action}</param>{NewLine}");
                }
                else
                {
                    _builder.Append(
                        $"/// <param name=\"{param.RawName}\">The <c>{param.RawTypeName}</c> value.</param>{NewLine}");
                }
            }
        }

        return this;
    }

    internal SourceBuilder AppendEmptyTripleSlashInheritdocComments(
        IndentationAdjustment adjustment = IndentationAdjustment.Noop)
    {
        AdjustIndentation(adjustment);
        var indent = _indentation.ToString();

        _builder.Append($"{indent}/// <inheritdoc />{NewLine}");

        return this;
    }

    internal SourceBuilder AppendTripleSlashInheritdocComments(
        string csharpTypeName,
        string memberName,
        IndentationAdjustment adjustment = IndentationAdjustment.Noop)
    {
        AdjustIndentation(adjustment);
        var indent = _indentation.ToString();

        _builder.Append($"{indent}/// <inheritdoc cref=\"{csharpTypeName}.{memberName}\" />{NewLine}");

        return this;
    }

    internal SourceBuilder AppendTripleSlashPropertyComments(
        CSharpProperty property,
        IndentationAdjustment adjustment = IndentationAdjustment.Noop)
    {
        AdjustIndentation(adjustment);
        var indent = _indentation.ToString();

        _builder.Append($"{indent}/// <summary>{NewLine}");

        var jsMethodName = property.RawName.LowerCaseFirstLetter();
        var func = $"{_options.Implementation}.{jsMethodName}";

        _builder.Append($"{indent}/// Source generated implementation of <c>{func}</c>.{NewLine}");
        var rootUrl = "https://developer.mozilla.org/docs/Web/API";
        var fullUrl = $"{rootUrl}/{_options.TypeName}/{jsMethodName}";
        _builder.Append($"{indent}/// <a href=\"{fullUrl}\"></a>{NewLine}");
        _builder.Append($"{indent}/// </summary>{NewLine}");

        return this;
    }

    internal SourceBuilder AppendLine()
    {
        // We use a hard-coded new line instead of:
        // _builder.AppendLine() as the new line value changes by environment.
        // For consistency, we'll always generate the exact same new line.
        _builder.Append(NewLine);

        return this;
    }

    internal SourceBuilder AppendRaw(
        string content,
        bool appendNewLine = true,
        bool postIncreaseIndentation = false,
        bool omitIndentation = false)
    {
        var indentation = omitIndentation ? "" : _indentation.ToString();
        _builder.Append($"{indentation}{content}{(appendNewLine ? NewLine : string.Empty)}");

        if (postIncreaseIndentation)
        {
            IncreaseIndentation();
        }

        return this;
    }

    internal SourceBuilder IncreaseIndentation()
    {
        IncreaseIndentationImpl(true);

        return this;
    }

    internal SourceBuilder AppendConditionalDelegateFields(List<CSharpMethod>? methods)
    {
        if (methods is { Count: > 0 })
        {
            foreach (var group in
                methods.SelectMany(m => m.ParameterDefinitions)
                    .Where(param => param.ActionDeclaration is not null)
                    .GroupBy(param => param.RawName))
            {
                var param = group.First();
                var typeArguments = param.ActionDeclaration!.MappedActionTypeArguments;

                var fieldName = $"_{param.RawName}";
                Fields ??= new HashSet<string>();
                Fields.Add(fieldName);

                // For a zero-parameter callback (e.g. TS `VoidFunction`)
                // the field type is the non-generic `Action` -- emitting
                // `Action<>?` would not compile. For non-empty cases,
                // route each generic argument through
                // `MappedActionTypeArguments` so the field uses C#
                // primitive spellings (e.g. `Action<double>` for a TS
                // `(time: number): void` callback) instead of leaking
                // raw TypeScript names.
                var fieldType = typeArguments.Count == 0
                    ? "Action"
                    : $"Action<{string.Join(", ", typeArguments)}>";

                AppendRaw($"private {fieldType}? {fieldName};");
            }

            AppendLine();
        }

        return this;
    }

    internal SourceBuilder AppendConditionalDelegateCallbackMethods(List<CSharpMethod>? methods)
    {
        if (methods is { Count: > 0 })
        {
            var level = IndentationLevel;

            foreach (var group in
                methods.SelectMany(m => m.ParameterDefinitions)
                    .Where(param => param.ActionDeclaration is not null)
                    .GroupBy(param => param.RawName))
            {
                var param = group.First();
                var methodName = $"On{param.RawName.CapitalizeFirstLetter()}";
                Methods ??= new HashSet<string>();
                if (Methods.Add(methodName))
                {
                    // Reset indentation so each [JSInvokable] block starts at the
                    // outer indentation level — without this reset, the previous
                    // iteration's `postIncreaseIndentation` leaks into the next one.
                    ResetIndentationTo(level);

                    var fieldName = $"_{param.RawName}";

                    AppendRaw("[JSInvokable]");
                    AppendRaw($"public void {methodName}(", postIncreaseIndentation: true);
                    AppendParameters(param, fieldName);
                }
            }

            AppendLine();
            ResetIndentationTo(level);
        }

        return this;
    }

    private SourceBuilder AppendParameters(CSharpType param, string fieldName)
    {
        var parameterDefinitions = param.ActionDeclaration!.ParameterDefinitions!;

        if (parameterDefinitions.Count == 0)
        {
            // Zero-parameter callback (e.g. TS `VoidFunction`, `(): void;`).
            // Close the open `(` left by the caller and emit the `Invoke`
            // expression -- without this, the iteration-based path below
            // silently skipped the loop body and left the `[JSInvokable]`
            // method definition truncated to `public void OnX(` with no
            // body, producing uncompilable output.
            AppendRaw(") =>", postIncreaseIndentation: true);
            AppendRaw($"{fieldName}?.Invoke();");

            return this;
        }

        var args = new List<string>();
        foreach (var (interation, p) in parameterDefinitions.Select())
        {
            // Escape if the callback parameter name collides with a C#
            // reserved keyword (real DOM precedent: callback params
            // named `event`, `class`, `default`, etc.). The argument
            // list passed to `_x?.Invoke(...)` uses the same escaped
            // name, so the call site and the parameter list stay
            // consistent.
            var paramName = p.RawName.EscapeCSharpKeyword();
            args.Add(paramName);
            // Route each parameter type through the same primitive-mapping
            // helper used for the `Action<...>` field signature, so the
            // shim's parameter list aligns with the field's generic
            // arguments. Previously this emitted `p.RawTypeName` directly
            // (e.g. `number time` for a TS `(time: DOMHighResTimeStamp)`
            // callback after alias resolution), which is not valid C#.
            var typeName = CSharpAction.MapParameterTypeToCSharp(p.RawTypeName);
            AppendRaw($"{typeName} {paramName}", appendNewLine: false);
            if (interation.HasMore)
            {
                AppendRaw(",");
            }
            else
            {
                AppendRaw(") =>", postIncreaseIndentation: true);
                AppendRaw($"{fieldName}?.Invoke({string.Join(", ", args)});");
            }
        }

        return this;
    }

    internal SourceBuilder DecreaseIndentation()
    {
        DecreaseIndentationImpl(true);

        return this;
    }

    internal SourceBuilder ResetIndentationTo(int level)
    {
        _indentation = _indentation.ResetTo(level);

        return this;
    }

    private void IncreaseIndentationImpl(bool increaseIndentation = false) =>
        AdjustIndentation(increaseIndentation
            ? IndentationAdjustment.Increase
            : IndentationAdjustment.Noop);

    private void DecreaseIndentationImpl(bool decreaseIndentation = false) =>
        AdjustIndentation(decreaseIndentation
            ? IndentationAdjustment.Decrease
            : IndentationAdjustment.Noop);

    private void AdjustIndentation(IndentationAdjustment adjustment) =>
        _indentation = adjustment switch
        {
            IndentationAdjustment.Increase => _indentation.Increase(),
            IndentationAdjustment.Decrease => _indentation.Decrease(),
            _ => _indentation
        };

    internal string ToSourceCodeString() => _builder.ToString();
}
